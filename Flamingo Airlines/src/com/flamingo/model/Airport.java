package com.flamingo.model;

public class Airport {

	private int airportId;
	private String airportNameString;
	private City city;
	
	public int getAirportId() {
		return airportId;
	}
	public void setAirportId(int airportId) {
		this.airportId = airportId;
	}
	public String getAirportNameString() {
		return airportNameString;
	}
	public void setAirportNameString(String airportNameString) {
		this.airportNameString = airportNameString;
	}
	public City getCity() {
		return city;
	}
	public void setCity(City city) {
		this.city = city;
	}
	
	
	
	
}
